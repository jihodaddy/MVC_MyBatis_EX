package com.spring_mvc_mybatis.gear.model;

public class GearVO {
	private String gearNo; 
    private String gearName;
    private String gearMaker; 
    private String gearCtg; 
    private int gearPrice;
    private int gearQty;
    
	public String getGearNo() {
		return gearNo;
	}
	public void setGearNo(String gearNo) {
		this.gearNo = gearNo;
	}
	public String getGearName() {
		return gearName;
	}
	public void setGearName(String gearName) {
		this.gearName = gearName;
	}
	public String getGearMaker() {
		return gearMaker;
	}
	public void setGearMaker(String gearMaker) {
		this.gearMaker = gearMaker;
	}
	public String getGearCtg() {
		return gearCtg;
	}
	public void setGearCtg(String gearCtg) {
		this.gearCtg = gearCtg;
	}
	public int getGearPrice() {
		return gearPrice;
	}
	public void setGearPrice(int gearPrice) {
		this.gearPrice = gearPrice;
	}
	public int getGearQty() {
		return gearQty;
	}
	public void setGearQty(int gearQty) {
		this.gearQty = gearQty;
	}
    
    
}
